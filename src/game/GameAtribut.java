package game;

public class GameAtribut {

	private String TimeDay,Description;

	public GameAtribut(String timeDay, String description) {
		super();
		TimeDay = timeDay;
		Description = description;
	}

	public String getTimeDay() {
		return TimeDay;
	}

	public void setTimeDay(String timeDay) {
		TimeDay = timeDay;
	}

	public String getDescription() {
		return Description;
	}

	public void setDescription(String description) {
		Description = description;
	}
	
	

}
